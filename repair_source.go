package isotopo

import (
	"fmt"
	"math"
)

// RepairSource runs the projection-repair loop (RepairScene) against the
// document parsed from src and persists the repairs BACK into the source text,
// comment-preserved — closing the loop that render-time repair leaves open:
// without this, `render` silently fixes occlusions/overlaps/contrast in memory
// on every run while `validate` keeps warning about them forever, because the
// author's file never learns about the fix.
//
// Mechanics: the doc is parsed twice; one copy is repaired; the two part trees
// are diffed field-by-field; each difference becomes a set-field edit op
// applied through ApplyOpText, so comments and formatting survive exactly as
// they do for Studio edits. By construction, loading the persisted source
// reproduces the repaired document state — persistence cannot behave
// differently from what `render` already draws today.
//
// Only YAML sources are supported: a .d2 file's geometry is generated by
// auto-layout, so there is nothing stable to write a repair into.
//
// Returns the new source (== src when nothing needed repair), the fix list
// from RepairAndReport, and an error only for parse/apply failures.
func RepairSource(lang string, src []byte) ([]byte, []string, error) {
	return RepairSourceWithOptions(lang, src, RepairOptions{})
}

// RepairOptions selects which repair passes run beyond the always-on defect
// loop. Compose additionally runs the composition pass (ComposeScene):
// bounded alignment snapping of explicitly-offset parts onto their
// neighbours' tracks — the acting half of the evaluate `composition` block.
type RepairOptions struct {
	Compose bool
}

// RepairSourceWithOptions is RepairSource with pass selection.
func RepairSourceWithOptions(lang string, src []byte, opts RepairOptions) ([]byte, []string, error) {
	if lang != "yaml" {
		return nil, nil, fmt.Errorf("repair: only yaml sources can be written back (got %q)", lang)
	}
	base, err := Parse(src)
	if err != nil {
		return nil, nil, fmt.Errorf("repair: parse: %w", err)
	}
	repaired, err := Parse(src)
	if err != nil {
		return nil, nil, fmt.Errorf("repair: parse: %w", err)
	}
	_, fixes := RepairAndReport(repaired)
	if opts.Compose {
		fixes = append(fixes, ComposeScene(repaired)...)
	}
	if len(fixes) == 0 {
		return src, nil, nil
	}

	ops := diffRepairOps(base, repaired)
	out := src
	for _, op := range ops {
		next, err := ApplyOpText(lang, out, op)
		if err != nil {
			return nil, fixes, fmt.Errorf("repair: persist %s on %q: %w", op.Kind, op.ID, err)
		}
		out = next
	}
	// Persistence verification: the whole contract is load(persisted) ==
	// repaired doc. Text surgery can silently miss that contract on exotic
	// formatting (a part's flow map spanning multiple lines once landed a
	// style block INSIDE geom) — so re-parse the result and re-diff it
	// against the repaired doc; any residue means the write did not land
	// cleanly and the caller gets an error instead of a corrupted file.
	achieved, err := Parse(out)
	if err != nil {
		return nil, fixes, fmt.Errorf("repair: persisted source no longer parses (%w) — the input's formatting defeats text surgery; reformat the affected part onto one line", err)
	}
	if residue := diffRepairOps(achieved, repaired); len(residue) > 0 {
		return nil, fixes, fmt.Errorf("repair: could not persist %d change(s) cleanly (first: %s on %q) — the affected part's formatting (e.g. a multi-line flow map) defeats text surgery; reformat it and re-run", len(residue), residue[0].Kind, residue[0].ID)
	}
	return out, fixes, nil
}

// diffRepairOps walks the two documents' part trees in parallel (repair never
// adds or removes parts, so the shapes match) and emits one set-field op per
// part whose repairable fields changed. Fields covered are exactly the ones
// the repair passes mutate: offset (overlap separation, neighbour-label
// lifts), layout.padding (caption clearance), style.text.color (contrast).
func diffRepairOps(base, repaired *Document) []EditOp {
	var ops []EditOp
	for id, bn := range base.Nodes {
		rn := repaired.Nodes[id]
		if bn == nil || rn == nil {
			continue
		}
		diffRepairParts(bn.Parts, rn.Parts, &ops)
	}
	return ops
}

func diffRepairParts(base, repaired []*CompositePart, ops *[]EditOp) {
	n := len(base)
	if len(repaired) < n {
		n = len(repaired)
	}
	for i := 0; i < n; i++ {
		b, r := base[i], repaired[i]
		if b == nil || r == nil {
			continue
		}
		if b.ID != "" && b.ID == r.ID {
			fields := map[string]string{}

			bx, by, bz := offsetOf(b)
			rx, ry, rz := offsetOf(r)
			if !feq(bx, rx) {
				fields["offset.wx"] = fnum(rx)
			}
			if !feq(by, ry) {
				fields["offset.wy"] = fnum(ry)
			}
			if !feq(bz, rz) {
				fields["offset.wz"] = fnum(rz)
			}

			if r.Layout != nil && r.Layout.Padding != nil {
				bp := math.NaN()
				if b.Layout != nil && b.Layout.Padding != nil {
					bp = *b.Layout.Padding
				}
				if math.IsNaN(bp) || !feq(bp, *r.Layout.Padding) {
					fields["layout.padding"] = fnum(*r.Layout.Padding)
				}
			}

			bc, rc := textColorOf(b), textColorOf(r)
			if rc != "" && rc != bc {
				fields["style.text.color"] = rc
			}

			if len(fields) > 0 {
				*ops = append(*ops, EditOp{Kind: "set-field", Target: "node", ID: b.ID, Fields: fields})
			}
		}
		diffRepairParts(b.Parts, r.Parts, ops)
	}
}

func offsetOf(p *CompositePart) (x, y, z float64) {
	if p.Offset != nil {
		return p.Offset.WX, p.Offset.WY, p.Offset.WZ
	}
	return 0, 0, 0
}

func textColorOf(p *CompositePart) string {
	if p.Style != nil && p.Style.Text != nil {
		return p.Style.Text.Color
	}
	return ""
}

func feq(a, b float64) bool { return math.Abs(a-b) < 1e-9 }

// MarkRepairable flags every issue in `before` that no longer appears in
// `after` (the validation of a repaired clone) as auto-repairable, so agents
// and humans can tell "this warning will be fixed by `isotopo repair --write`
// (and is already auto-fixed at render time)" apart from issues that need a
// hand edit. Matching is exact on severity+path+message.
func MarkRepairable(before, after []Issue) []Issue {
	remaining := make(map[string]bool, len(after))
	for _, is := range after {
		remaining[issueKey(is)] = true
	}
	out := make([]Issue, len(before))
	for i, is := range before {
		if !remaining[issueKey(is)] {
			is.Repairable = true
		}
		out[i] = is
	}
	return out
}

func issueKey(is Issue) string {
	return string(is.Severity) + "\x00" + is.Path + "\x00" + is.Message
}
